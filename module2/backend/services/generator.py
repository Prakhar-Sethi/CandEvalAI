def _generate_text(prompt: str, max_new_tokens: int = 200) -> str:
    if _tokenizer is None or _model is None:
        return ""
    try:
        import torch
        inputs = _tokenizer(prompt, return_tensors="pt", truncation=True, max_length=512)
        with torch.no_grad():
            outputs = _model.generate(
                **inputs,
                max_new_tokens=max_new_tokens,
                do_sample=True,       # enables randomness
                temperature=0.9,      # controls diversity
                top_p=0.92,           # nucleus sampling
                repetition_penalty=1.3,
            )
        return _tokenizer.decode(outputs[0], skip_special_tokens=True)
    except Exception as e:
        logger.warning(f"Generation error: {e}")
        return ""


def _parse_mcq(raw: str) -> Optional[dict]:
    """Parse flan-t5 output into MCQ dict."""
    try:
        q_match = re.search(r'Question:\s*(.+?)(?=A\))', raw, re.DOTALL | re.IGNORECASE)
        a_match = re.search(r'A\)\s*(.+?)(?=B\))', raw, re.DOTALL)
        b_match = re.search(r'B\)\s*(.+?)(?=C\))', raw, re.DOTALL)
        c_match = re.search(r'C\)\s*(.+?)(?=D\))', raw, re.DOTALL)
        d_match = re.search(r'D\)\s*(.+?)(?=Answer:)', raw, re.DOTALL)
        ans_match = re.search(r'Answer:\s*([ABCD])', raw, re.IGNORECASE)

        if not all([q_match, a_match, b_match, c_match, d_match, ans_match]):
            return None

        options = {
            "A": a_match.group(1).strip(),
            "B": b_match.group(1).strip(),
            "C": c_match.group(1).strip(),
            "D": d_match.group(1).strip(),
        }
        answer_letter = ans_match.group(1).upper()
        return {
            "question_text": q_match.group(1).strip(),
            "options": options,
            "answer_key": options[answer_letter],
        }
    except Exception:
        return None


def _parse_short_answer(raw: str) -> Optional[dict]:
    """Parse flan-t5 output into short-answer dict."""
    try:
        q_match = re.search(r'Question:\s*(.+?)(?=Expected Answer:)', raw, re.DOTALL | re.IGNORECASE)
        a_match = re.search(r'Expected Answer:\s*(.+)', raw, re.DOTALL | re.IGNORECASE)
        if not q_match or not a_match:
            return None
        return {
            "question_text": q_match.group(1).strip(),
            "options": None,
            "answer_key": a_match.group(1).strip(),
        }
    except Exception:
        return None


def _get_fallback_pool(skill: str, difficulty: str) -> list[dict]:
    """
    Return question bank entries for the skill, filtered by difficulty.
    Falls back to any difficulty if the specific difficulty has too few entries.
    Falls back to Python bank if skill is unknown.
    """
    bank = QUESTION_BANK.get(skill) or QUESTION_BANK.get("Python", [])
    filtered = [q for q in bank if q.get("difficulty") == difficulty]
    # If fewer than 3 matching difficulty, use the full bank for that skill
    if len(filtered) < 3:
        filtered = bank
    return filtered


def generate_questions(skill: str, difficulty: str, num: int = 3) -> list[dict]:
    """
    Generate `num` questions for a skill at the given difficulty.
    Uses curated question bank for fast, reliable generation.
    """
    import os
    pool = _get_fallback_pool(skill, difficulty)
    random.shuffle(pool)

    results = []
    for i, fb in enumerate(pool[:num]):
        results.append({
            "type": fb["type"],
            "question_text": fb["question"],
            "options": fb.get("options"),
            "answer_key": fb.get("answer_text", fb.get("answer", "")),
        })

    # Pad with any-difficulty questions if pool was too small
    if len(results) < num:
        extra_pool = [q for q in QUESTION_BANK.get(skill, QUESTION_BANK.get("Python", []))
                      if q not in pool]
        random.shuffle(extra_pool)
        for fb in extra_pool[:num - len(results)]:
            results.append({
                "type": fb["type"],
                "question_text": fb["question"],
                "options": fb.get("options"),
                "answer_key": fb.get("answer_text", fb.get("answer", "")),
            })

    return results
